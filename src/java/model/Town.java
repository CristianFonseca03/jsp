package model;

/**
 *
 * @author User
 */
public class Town {

    private String name;
    private String code;
    private String department;

    public Town(String name, String code, String department) {
        this.name = name;
        this.code = code;
        this.department = department;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    @Override
    public String toString() {
        return "ExampleTown{" + "name=" + name + ", code=" + code + ", department=" + department + '}';
    }

}
